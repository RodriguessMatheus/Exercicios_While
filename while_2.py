"""Faça um programa while que calcule o mostre a média aritmética de N notas."""

n = int(input("Digite quantas notas deseja inserir: "))
i = 0
soma = 0
while i < n:
  nota = float(input("Digite a nota: "))
  soma += nota
  i += 1
media = soma / n
print("A média das notas é: ", media)

